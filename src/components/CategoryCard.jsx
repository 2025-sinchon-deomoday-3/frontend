const CategoryCard = ({ categoryData }) => {
  const categoryLabel = categoryData.label; //한글레이블 가져오는 거!

  const formatAmount = (amount) => {
    return parseFloat(amount).toLocaleString("en-US", {
      minimumFractionDigits: 0,
      maximumFractionDigits: 2,
    });
  };

  // 화폐 기호 매핑
  const getCurrencySymbol = (code) => {
    const symbols = {
      USD: "$",
      KRW: "₩",
      EUR: "€",
      JPY: "¥",
      GBP: "£",
      CNY: "¥",
      CAD: "CA$",
      TWD: "NT$",
    };
    return symbols[code] || code;
  };

  // 예산대비 메시지 (API 명세에 있는대로 썼음)
  const getBudgetMessage = () => {
    if (!categoryData.budget_diff) return "세부설정예산 없음";

    const diff = parseFloat(categoryData.budget_diff.foreign_amount);
    if (diff === 0) return "세부설정예산 없음";

    const symbol = getCurrencySymbol(categoryData.budget_diff.foreign_currency);

    // API 응답: 양수값이면 덜쓴거, 음수면 더쓴거
    if (diff > 0) {
      // 예산보다 적게 씀 (좋음)
      return `예산대비: - ${symbol}${formatAmount(Math.abs(diff))}`;
    } else {
      // 예산보다 많이 씀 (나쁨)
      return `예산대비: + ${symbol}${formatAmount(Math.abs(diff))}`;
    }
  };

  const budgetMessage = getBudgetMessage();
  const isOverBudget =
    categoryData.budget_diff &&
    parseFloat(categoryData.budget_diff.foreign_amount) < 0; // 음수면 초과
  const hasNoBudget = budgetMessage === "세부설정예산 없음";

  return (
    <Card>
      <CardHeader>
        <IconWrapper>
          <CategoryIcon category={categoryData.code} />
        </IconWrapper>
        <CategoryName>{categoryLabel}</CategoryName>
      </CardHeader>

      <CardBody>
        <AmountSection>
          <PrimaryAmount>
            {getCurrencySymbol(categoryData.foreign_currency)}
            {formatAmount(categoryData.foreign_amount)}
          </PrimaryAmount>
          <SecondaryAmount>
            {formatAmount(categoryData.krw_amount)}원
          </SecondaryAmount>
        </AmountSection>

        <BudgetStatus $isOver={isOverBudget} $hasNoBudget={hasNoBudget}>
          {budgetMessage}
        </BudgetStatus>
      </CardBody>
    </Card>
  );
};

